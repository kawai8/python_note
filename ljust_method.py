# ljust method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.ljust

"""
[Syntax]
str.ljust(width[, fillchar])

[Parameter]
width: width of the returned string
fillchar: padding character

[Return]
a lefted string of length width padded with specified fillchar
"""

# ------------------------------------------
# Example
# ------------------------------------------
print('apple'.ljust(8, '_'))
# output: apple___
