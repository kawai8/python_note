# capitalize method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.capitalize

"""
[Syntax]
str.capitalize()

[Parameter]
none 

[Return]
a copy of the string with its first character capitalized and the rest lowercased
"""

# ------------------------------------------
# Example
# ------------------------------------------
print('python programmer'.capitalize())
# 'Python programmer'
                                                                                               
