# lower method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.lower

"""
[Syntax]
str.lower()

[Parameter]
none 

[Return]
a copy of the string with all the cased characters converted to lowercase.
"""

# ------------------------------------------
# Example
# ------------------------------------------
print('PYTHON'.lower())
# output: python

print('ＰＹＴＨＯＮ'.lower())
# output: ｐｙｔｈｏｎ
