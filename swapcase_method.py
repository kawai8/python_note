# swapcase method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.swapcase

"""
[Syntax]
str.swapcase()

[Parameter]
none 

[Return]
a copy of the string with uppercase characters converted to lowercase and vice versa"""

# ------------------------------------------
# Example
# ------------------------------------------
print('Python Programmer'.swapcase())
# output: pYTHON pROGRAMMER
