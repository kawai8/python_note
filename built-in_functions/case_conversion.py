## upper method
'python'.upper()
# 'PYTHON'
'ｐｙｔｈｏｎ'.upper() # two-byte character
# 'ＰＹＴＨＯＮ'


# lower method
'PYTHON'.lower()
# 'python'
'ＰＹＴＨＯＮ'.lower() # two-byte character
# 'ｐｙｔｈｏｎ'


# swapcase method
'Hello Python'.swapcase()
# 'hELLO pYTHON'


# capitalize method
'python programmer'.capitalize()
# 'Python programmer'


# title method
'python programmer'.title()
# 'Python Programmer'

