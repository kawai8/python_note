# rjust method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.rjust

"""
[Syntax]
str.rjust(width[, fillchar])

[Parameter]
width: the width of the returned string 
filchar: padding character

[Return]
the string right justified in a string of length width.
"""

# ------------------------------------------
# Example
# ------------------------------------------
print('programmer'.rjust(20, '*'))
# output: **********programmer
