# center method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.center

"""
[Syntax]
str.center(width[, fillchar])

[Parameter]
width: length of the string
fillchar: padding character

[Return]
a centered string of length width padded with specified fillchar
"""

# ------------------------------------------
# Example
# ------------------------------------------
print('apple'.center(8, '_'))
# output: _apple__
                                                                               
