# title method
# str class
# https://docs.python.org/3/library/stdtypes.html#str.title

"""
[Syntax]
str.title()

[Parameter]
none 

[Return]
a titlecased version of the string where words start with an uppercase character and the remaining characters are lowercase
"""

# ------------------------------------------
# Example
# ------------------------------------------
print('python programmer'.title())
# output: Python Programmer

print('漢字aaa'.title())
# output: 漢字Aaa
